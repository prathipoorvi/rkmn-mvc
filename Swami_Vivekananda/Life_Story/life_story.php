<h2>&nbsp;</h2>
<ol>
	<li>
		Vivekananda was born in 1863 to a rich family. Through his childhood he displayed outstanding qualities of character, devotion, talent, a deep philosophical mind, and a capacity for both deep meditation and intense compassion for his follow men.
		<p>For more details on this period click here<br /><strong>Master and Disciple</strong></p>	
	</li>
	<li>In college, he explored Western Philosophy and at the same time, was attracted by the ideal of renunciation embodied in the Great Rishis of India.
		<p>It was a deep spiritual urge that took him to him to Sri Ramakrishna with the question, 'Have you seen God?'</p>
		<p>For more details on this period click here</p>
	</li>
	<li>
		In the next few years, the extraordinary bond between Sri Ramakrishna and Narendra Nath (the future Swami Vivekananda) was revealed. The final years of Sri Ramakrishna's life is also the fascinating story of the quelling of a great and rebellious spirit by an even greater master.
		<p>For more details on this period click here</p>
	</li>
	<li>
		In 1885, after the passing away of Sri Ramakrishna, Swami Vivekananda plunged into the life of austerity and disciplines that saw the shaping of the future World Transformer - Swami Vivekananda. This phase included the early years at Barangore Math, the wandering as a 'nameless monk' across the length and breadth of India, the befriending of princes and commoners, and the extraordinary realizations at Kanyakumari.
		<p>For more details on this period click here<br /><strong>The Vivekananda Rock at Kanyakumari where Swamji meditated</strong></p>
	</li>
	<li>
		At the end of this phase, began the 'period of contact' between Swami Vivekananda and a world hungry and thirsty for ideas to rejuvenate it. The new beginning was heralded by the triumphant addresses at the Chicago "Parliament of Religion". This was 1893.
		<p>For more details on this period click here</p>
		<p>For Swamiji's addresses at the Parliament click here<br />Parliament of Religions<br />1893: Indian Disciples at Chicago</p>
	</li>
	<li>
		In the next few years, Swami Vivekananda thundered forth the life giving, man making ideas to a West that not only applauded him but also gave him a core group of disciples that would carry the work forward in that part of the world and aid the work in India.
		<p>For more details on this period click here</p>
	</li>
	<li>
		Swami Vivekananda returned in 1896 to India. The series of lectures given from Colombo to Almora, to an adulating audience every where, presented to his motherland a new vision of India - an India that builds on its thousands of years of spiritual strivings and truth seeking and also integrates itself into a modern, vibrant society that meets the world, not as a mendicant but as a Heroic Nation in its own right.
		<p>For more details on this period click here</p>
	</li>
	<li>
		Swami Vivekananda, thus put his work into a firm institutional framework with the setting up of the Ramakrishna Mission and Ramakrishna Math.
		<p>For more details on this period click here</p>
	</li>
	<li>
		Swami Vivekananda visited the West a second time in 1899, this time with Swami Turiyananda and Sister Nivedita. He not only consolidated his work by starting the Vedanta Society of San Francisco and Shanti Ashrama at Northern California but also saw with piercing clarity, that "Social life in the West is like a peal of laughter, but underneath, it is a wail. It ends in a sob. The fun and frivolity are all on the surface: really it is full of tragic intensity." His primary message was "Yes, the older I grow, the more everything seems to me to lie in manliness." 
	</li>
	<li>
		Swami Vivekananda returned to India towards of 1900. The next one and half years, were spent not only in travelling and meeting people, but also in Belur Math with brother disciples and young novitiates.
		<p>For more details on this period click here<br />At Pasadena with Western disciples, 1900</p>
	</li>
	<li>
		Swamiji's extraordinary life on earth came to an end on July 4, 1902. He said "It may be that I shall find it good to get outside my body - to cast it off like a worn-out garment. But I shall not cease to work! I shall inspire men everywhere, until the world shall know that it is one with God!
		<p>For more details on this period click here<br />I shall not cease to work!</p>
	</li>
