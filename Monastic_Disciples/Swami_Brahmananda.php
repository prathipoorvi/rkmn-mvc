<h1>Swami Brahmananda</h1>
<h2>An introduction</h2>
<h3>Monastic Name: Swami Brahmananda</h3>
<h3>Pre-Monastic Name: Rakhal Chandra Ghosh</h3>
<h4>January 1863</h4>
<p>Born on 21st January and was named Rakhal (meaning a 'cowherd boy' - playmate of Krishna)</p>
<h4>Middle of 1881</h4>
<p>Met Sri Ramakrishna for the first time. In this year, he married an 11 year sister of Manomohan Mittra who was a devotee of Sri Ramakrishna, who took Rakhal to Sri Ramakrishna, and who also later made it possible for him to renounce the world.</p>
<h4>January 1886</h4>
<p>Got ochre cloth (monastic robes) and rosaries distributed by the Master among 12 of his disciples.</p>
<h4>January 1887</h4>
<p>Took final monastic vows with other brother disciples by performing the traditional viraja homa in front of the Master's picture.</p>
<h4>March 1888 - 1892</h4>
<p>Travelled and went on pilgrimages spending a lot of time practising meditation, relying solely on God for all needs - including the next meal.</p>
<h4>May 1897</h4>
<p>Formed the Ramakrishna Mission with Swami Vivekananda and other disciples. Swami Vivekananda became general president and Swami Brahmananda - the president of the Kolkata Centre. Handled accounts, kept the monastery's diary, supervised the monastery and gave spiritual instructions to the novices.</p>
<h4>1904</h4>
<p>Became the President of the Mission after the passing away of Swami Vivekananda.</p>
<h4>During his presidency</h4>
<p>Over saw the development of The Ramakrishna Math and Ramakrishna Mission into a strong and vibrant institution with numerous centres.</p>
<h4>10 April 1922</h4>
<p>Passed away at Balaram Mandir, Kolkata.</p>
