<h3>Public Library and Reading hall</h3>
<h4>About the Library</h4>
<p>The Library which was started in a small room with only 50 books in 1932 is now housed in a spacious building. It contains large number of books on various subjects like philosophy, religion, education, culture, literature, sociology, history, arts, science, biographies, agriculture etc. These books are in various languages such as Sanskrit, Marathi, Hindi, English, Bengali, etc. The library has separate sections for reference books as well as books for issue which can easily be accessed by open access computer system. There are also separate study rooms in this Library. There is a free Reading Hall on the first floor which provides various newspapers and periodicals to the readers.</p>
<h4>A Brief History</h4>
<ul>
	<li><strong>1932</strong> marked the beginning of the Library in a small room with 50 books only.  Some of these books were purchased and others were collected.</li>
	<li><strong>1936</strong> According to Srimat Swami Bhaskareshwaranandajiâ€™s report, the number of books in the library became 2597.  Besides these books, there were periodicals out of which 11 were monthly, 9 weekly, 2 bi-weekly, 3 tri-weeklies and one quarterly.  Priority was given to English, Marathi, Hindi, Bengali and Sanskrit books and periodicals.  There were four newspapers.  According to the report, the total number of library members was 277, out of which 90 members were exempted from paying fees.</li>
	<li><strong>1953</strong> As the number of books and members increased to a great extent, the need for a separate building for the library was felt acutely.  A plot of land in the close vicinity to the Ashrama was quite suitable for the construction of library building.  As the said land belonged to Central Jail, the state government was approached for procuring the land.  The then Chief Minister of Madhya Pradesh, Shri Ravishankar Shukla used his authority to make it available to the Ashrama. Grants for the construction of the library building and also for the purchase of books were received both from the state and central governments.  Some amount of the grants from the central government remained unutilized and so the said amount was returned to the respective government.  This action of the Ashrama was very much appreciated by the then Central Minister for Education, Maulana Abdul Kalam Azad.</li>
	<li><strong>1959</strong> Two story building was constructed on the land purchased by the Math. The Library and reading room has been shifted in this building. It was inaugurated by then Chief Minister of Maharashtra Sri Yashwantraoji Chauhan. Revered Swami Sambuddhanandaji Maharaj, President of Ramakrishna Math, Mumbai was the Chief Guest of the function.</li>
	<li><strong>2006</strong> The total number of books in the library rose to 46934.  These include books in English, Marathi, Hindi Bengali, Sanskrit and a few other languages, on the subjects like Religion, Philosophy, Language and Literature, Biology, Science, Social Sciences, Education, Art and Culture, etc.. These include reference books also.  There are many periodicals some of which are from foreign countries.  Total number of members is 1500.  In the Reading Hall there are 25 newspapers and 97 periodicals.  About 125 people visit the Reading hall for reading newspapers and periodicals.  The library remains open in the morning a well as in the evening for five hours.  Most of those who take advantage of the reference books are teachers and students. The library has received the participation of the following Swamijis as Library-in-charge:</li>
</ul>
