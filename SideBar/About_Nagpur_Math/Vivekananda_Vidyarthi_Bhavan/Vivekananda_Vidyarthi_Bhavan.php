<h3>Vivekananda Vidyarthi Bhavan</h3>
<h4>About the Vivekananda Vidyarthi Bhavan</h4>
<p> This Students' Hostel was started in 1932 with two students. Students taking education in various colleges affiliated to the Nagpur University are admitted in this Hostel. At present it can accommodate 20 students.</p>
<p>The Ashrama bears all the expenses of lodging and boarding of some poor and deserving students while some others students are given concession in the monthly charges. Special attention is paid to the physical, intellectual and moral development of the students, bearing in mind that they are the future citizens of India. The students have a Study circle which organizes various programmes like debates, symposia, extempore speeches etc. Every year the Study Circle celebrates Saraswati Puja for three days from Vasant Panchami onwards, with a variety of programmes such as Symposium, magic-lantern lecture, lectures of eminent scholars, Bhajan etc. Books are presented to students who are successful in physical and intellectual competitions. The students also have the facility of games. There is also a Text-book library in the Hostel. The students also avail of the large Library and the Reading Hall of the Ashrama. Weekly religious classes are held for inculcating moral and spiritual values among the students.</p>
<p>The students who have spent a few years in this Hostel have become responsible citizens and many of them are serving the Society in their respective fields of activities</p>
<h4>A Brief History</h4>
<ul>
	<li>1932 marked the beginning of the hostel for students taking education in various colleges affiliated to Nagpur University in the premises of the Ashram.  As most of the colleges in Nagpur merely impart information to their students and do not make any attempt for the all-round development of their personality, the Vidyarthi Bhavan conducts various activities for the physical, intellectual and moral development of its students.  According to a brief report of Srimat Swami Bhaskareshwaranandaji, only four students were admitted in the Bhavan at the beginning.  Out of these four, a student named Shri Ramprasad Mishra, who was an orphan, stood first in order of merit at the B.A. Examination of Nagpur University.  He was awarded four medals, three gold and silver, by the University for his Academic Excellence</li>
	<li>1936 As per the above-mentioned report, the number of students was increased to eight.  Some poor and deserving students were fully exempted from paying hostel charges while some others were given half concession.  All the expenses on their account were borne by the Ashram.</li>
	<li>1948 Keeping in view the increasing number of activities in the Ashram, it was decided to shift the hostel to a separate building.  For the purpose, a double-storeyed building was constructed on the open piece of land to the north of the garden in the western part of the Ashram.  A room with a veranda on the ground floor of the building was used for the hostel.  The hall on the first floor was named as “Shivananda Sabhagriha” where all the programmes of the Ashram and the Vidyarthi Bhavan were held during the period between 1947 and 2006.  The new building was inaugurated at the hands of Srimat Swami Pavitranandaji Maharaj on the day of Bhagwan Sri Ramakrishna’s Birth Anniversary.</li>
	<li>1966 The number of students in the Bhavan was gradually raised to 22.  During the period between 1966 and 1978, thirty three students were admitted. Considering the increasing number of guests in the Ashram, the number of students was reduced to 22.  For the study of Vivekananda literature, a class is conducted specially for the hostel students.  The students have a study circle which organizes programmes like debates, symposia, extempore speeches, general knowledge competition, etc.  The study circle also celebrates Buddha Jayanti, Shankaracharya Jayanti, Christmas Eve, Saraswati Puja etc. The attendance of the hostel students is compulsory in the prayer meetings and all other public functions held in the Ashram. The students make all the necessary arrangements for the smooth conduct of these functions.</li>
	<li>2004 Out of the 22 students, 3 students were given full concession in monthly charges and 14 others were given half concession. Then student home was shifted on first floor of Sarada Dham where there were 11 students.</li>
	<li>2005 In view of the increasing number of students needing hostel accommodation, a piece of land belonged to Central Jail measuring about 2 acres adjacent to the library building was purchased from Government of Maharashtra. A sale deed in the name of the President, Sri Ramakrishna Ashram, Nagpur was executed and the said land was handed over.</li>
	<li>2007 Foundation stone was laid down by Srimat Swami Atmasthanandaji Maharaj, Vice President of Ramakrishna Order, on 22nd January 2007 where hostel building big enough for fifty students will soon be constructed. The Vidyarthi Bhavan has been greatly benefited by the participation of the following Swamijis as its wardens</li>
</ul>
