<h3>Physiotherapy Unit</h3>
<h4>About the Physiotherapy Unit</h4>
<p>The Math constructed a separate building for charitable Physiotherapy Unit. The unit is housed with all modern exercising instruments. Three Physiotherapists assisted by 2 assistants and more than 3 to 4 volunteers are managing the unit under the supervision of one monastic monk</p>
<h4>New equipments in Physiotherapy unit of Ramakrishna Math, Nagpur</h4>
<p>Rotary Club, Nagpur donated New equipments to Physiotherapy unit of Ramakrishna Math, Nagpur on 1st March 2007. Rev.Swami Brahmasthananda, Swami Vipapmananda (Monk Incharge of Physiotherapy unit), authorities of Rotary Club Nagpur were present.</p>
