<h3>Mobile Dispensary</h3>
<p>The Math purchased one Matador in the year 1986 and started Mobile Dispensary. With the doctors and the other paramedical staff of 3 persons. The vehicle travels average 70 kms. per day. Every day on Bhandara, Umred and Koradi routes and covers more than 75 villages in a week.</p>
