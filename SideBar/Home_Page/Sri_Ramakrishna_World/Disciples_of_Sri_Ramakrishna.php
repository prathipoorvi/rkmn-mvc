<h3>This picture is a collage of the all the Monastic Disciples and some of the other intimate disciples of Sri Ramakrishna.</h3>
<h3>Monastic Disciples</h3>
<p>Sri Ramakrishna had 16 Monastic Disciples. All 16 are featured in the adjacent collage.</p>
<p>If you pass your mouse over the images, the names of the disciples will come up. You will be able to click on the image, and read more about the life of the monastic disciple.</p>
<h3>Some intimate disciples</h3>
<p>There are 8 intimate disciples featured here. This list is by no means comprehensive. For example, Swami Chetanananda's book "They lived with God" features a total of 28.</p>
